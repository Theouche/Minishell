#include "minishell.h"

char	give_fq(char fq, char c)
{
	if (c == 39 && fq == 39)
		fq = 0;
	else if (c == 34 && fq == 34)
		fq = 0;
	else if (fq == 0)
		fq = c;
	return (fq);
}
